import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { build } from 'esbuild'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const projectRoot = path.resolve(__dirname, '..')

const entryPath = path.join(
  projectRoot,
  'electron/remote-agent/runtime/agent-main-cli.ts',
)
const protocolPath = path.join(projectRoot, 'electron/remote-agent/protocol.ts')
const outputModulePath = path.join(
  projectRoot,
  'electron/remote-agent/runtime/generated-payload.ts',
)

function extractProtocolVersion(protocolSource) {
  const match = protocolSource.match(
    /REMOTE_AGENT_PROTOCOL_VERSION\s*=\s*['"]([^'"]+)['"]/
  )
  if (!match) {
    throw new Error('Failed to extract REMOTE_AGENT_PROTOCOL_VERSION from protocol.ts')
  }
  return match[1]
}

const protocolSource = await readFile(protocolPath, 'utf8')
const protocolVersion = extractProtocolVersion(protocolSource)

const bundleResult = await build({
  entryPoints: [entryPath],
  bundle: true,
  platform: 'node',
  format: 'cjs',
  target: 'node20',
  write: false,
  sourcemap: false,
  legalComments: 'none',
})

if (!bundleResult.outputFiles || bundleResult.outputFiles.length !== 1) {
  throw new Error('Expected a single bundled output for remote agent runtime.')
}

const bundledRuntime = bundleResult.outputFiles[0].text
const payload = `#!/usr/bin/env node\n${bundledRuntime}`

const moduleSource = `/* AUTO-GENERATED by scripts/build-remote-agent-runtime.mjs. DO NOT EDIT MANUALLY. */\n\nexport const REMOTE_AGENT_RUNTIME_PAYLOAD_PROTOCOL_VERSION = ${JSON.stringify(
  protocolVersion,
)} as const\n\nexport const REMOTE_AGENT_RUNTIME_PAYLOAD = ${JSON.stringify(payload)} as const\n`

await mkdir(path.dirname(outputModulePath), { recursive: true })
await writeFile(outputModulePath, moduleSource, 'utf8')
